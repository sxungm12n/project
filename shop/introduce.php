            <p>연평균 기온이 8~11℃ 정도의 서늘하고 건조한 지역이 사과 재배에 적당한 곳이다. 생육시기별 구분하면 최저기온이 -30℃ 이하로 내려가지 않고 생육기에는 18~24℃ 정도가 적당하다. 가을 수확기에는 일교차가 큰 지역이 좋다.</p>

